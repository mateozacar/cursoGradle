package co.ceiba.domain;

public class Person {
	
	 private String name;
	 private String Lastanem;
	 
	 public Person(String name, String Lastanem){
		 this.name = name;
		 this.Lastanem = Lastanem;
		 
	 }
	 
	public String getName() {
		return name;
	}

	public String getLastanem() {
		return Lastanem;
	}

}
